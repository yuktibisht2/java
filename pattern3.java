public class pattern3 {
    
        public static void main(String[] args)
        {
            int n =7;
            for (int i=0;i<n;i++)
            {
                for(int j=0; j<n;j++)
                {
                    System.out.print(i+1 + " ");
                }
                // ln takes cursor to new line
                System.out.println(" ");
                
            }
        }
        }

